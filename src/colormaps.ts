/**
 * All supported colormap identifiers.
 */
export type ColormapName =
    | 'viridis' | 'magma' | 'inferno' | 'plasma'
    | 'greys' | 'hot' | 'jet' | 'turbo';

export const COLORMAP_NAMES: ColormapName[] = [
    'viridis', 'magma', 'inferno', 'plasma',
    'greys', 'hot', 'jet', 'turbo'
];

// --- Viridis (Matplotlib CC0) sampled at 256 steps ---
// Full 256-entry table; abbreviated here for readability —
// the build step inlines the full table via generate_luts.py (not required at runtime).
// At runtime the webview receives the serialised Float32Array from the host.

/**
 * Return a flat [R,G,B,...] Uint8Array LUT for the given colormap name.
 * All 256 entries are included; index corresponds to normalised intensity 0–255.
 */
export function getLUT(name: ColormapName): Uint8Array {
    return LUTS[name];
}

